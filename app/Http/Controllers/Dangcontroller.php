     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $post=$request()->except(['_token']);
        $res=DB::table('dang')->where(['name'=>$post['name']])->first();
        if($post['pass']!=decrypt($res->pwd)){
            return redirect('/user/index')->with('msg','密码错误');
        }
        if($res){
            session(['dang'=>$res]);
            request()->session()->save();
            return redirect('/exam');
        }
        return redirect('/create')->with('msg','没有此用户 请联系管理员');
    }


